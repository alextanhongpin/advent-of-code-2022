var shapeScores = map[string]int{
	"A": 1,
	"B": 2,
	"C": 3,
	"X": 1,
	"Y": 2,
	"Z": 3,
}

var outcomeScores = map[string]int{
	"draw": 3,
	"win":  6,
}

func main() {
	fmt.Println(part1(input1))
	fmt.Println(part1(input2))
	fmt.Println(part2(input1))
	fmt.Println(part2(input2))
}

func playAgainst(rockPaperScissors string) (win, draw, lose string) {
	switch rockPaperScissors {
	case "A": // Rock
		return "B", "A", "C"
	case "B": // Paper
		return "C", "B", "A"
	case "C": // Scissors
		return "A", "C", "B"
	default:
		panic("invalid")
	}
}

func part1(s string) int {
	rpsByPlayer := map[string]string{
		"X": "A",
		"Y": "B",
		"Z": "C",
	}
	var scores int
	rows := parse(s)
	for _, row := range rows {
		parts := strings.Fields(row)
		opp, you := parts[0], parts[1]
		w, _, d := playAgainst(opp)
		you = rpsByPlayer[you]
		switch you {
		case w:
			scores += outcomeScores["win"]
		case d:
			scores += outcomeScores["draw"]
		}
		scores += shapeScores[you]
	}
	return scores
}

func part2(s string) int {
	var scores int
	rows := parse(s)
	for _, row := range rows {
		parts := strings.Fields(row)
		opp, outcome := parts[0], parts[1]

		w, d, l := playAgainst(opp)
		switch outcome {
		case "X": // Lose
			scores += shapeScores[l]
		case "Y":
			// Draw
			scores += outcomeScores["draw"]
			scores += shapeScores[d]
		case "Z":
			// Win
			scores += outcomeScores["win"]
			scores += shapeScores[w]
		}
	}
	return scores
}

func parse(s string) []string {
	return strings.Split(s, "\n")
}

func toInt(s string) int {
	n, err := strconv.Atoi(s)
	if err != nil {
		panic(err)
	}
	return n
}

