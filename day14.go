func getMinMax(grid map[point]bool) (minX, maxX, minY, maxY int) {
	minX, minY = math.MaxInt, math.MaxInt

	for p := range grid {
		if p.x < minX {
			minX = p.x
		}
		if p.x > maxX {
			maxX = p.x
		}
		if p.y < minY {
			minY = p.y
		}
		if p.y > maxY {
			maxY = p.y
		}
	}
	return
}

func parse(input string) map[point]bool {
	res := make(map[point]bool)
	rows := strings.Split(input, "\n")
	for _, row := range rows {
		coords := strings.Split(row, " -> ")
		prev := coords[0]
		for i := 0; i < len(coords[1:]); i++ {
			next := coords[1:][i]
			left, right := newPoint(prev), newPoint(next)

			if left.x == right.x {
				if left.y > right.y {
					left, right = right, left
				}
				l := left
				for y := left.y; y <= right.y; y++ {
					res[l] = true
					l.y++

				}
			}
			if left.y == right.y {
				if left.x > right.x {
					left, right = right, left
				}
				l := left
				for x := left.x; x <= right.x; x++ {
					res[l] = true
					l.x++
				}
			}
			prev = next
		}
	}
	return res
}

type point struct {
	x, y int
}

func newPoint(in string) point {
	parts := strings.Split(in, ",")
	return point{
		x: toInt(parts[0]),
		y: toInt(parts[1]),
	}
}

func toInt(s string) int {
	n, err := strconv.Atoi(s)
	if err != nil {
		panic(err)
	}
	return n
}

