type point struct {
	x, y int
}

func (p point) isDisconnected(o point) bool {
	return abs(p.x-o.x) == 2 || abs(p.y-o.y) == 2
}

func (p point) next(o point) point {
	y := o.y - p.y
	x := o.x - p.x
	return point{
		x: int(math.Copysign(float64(min(abs(x), 1)), float64(x))),
		y: int(math.Copysign(float64(min(abs(y), 1)), float64(y))),
	}
}

func abs(n int) int {
	if n < 0 {
		return -n
	}
	return n
}

func part1(input string) int {
	var head, tail point

	cache := make(map[point]bool)
	cache[tail] = true

	rows := parse(input)
	for _, row := range rows {
		parts := strings.Fields(row)
		dir, n := parts[0], toInt(parts[1])
		for i := 0; i < n; i++ {
			switch dir {
			case "U":
				head.y--
			case "D":
				head.y++
			case "L":
				head.x--
			case "R":
				head.x++
			}
			if tail.isDisconnected(head) {
				p := tail.next(head)
				tail.x += p.x
				tail.y += p.y
			}
			cache[tail] = true
		}
	}
	return len(cache)
}

func part2(input string) int {
	cache := make(map[point]bool)
	ropes := make([]point, 10)
	cache[ropes[0]] = true

	rows := parse(input)
	for _, row := range rows {
		parts := strings.Fields(row)
		dir, n := parts[0], toInt(parts[1])
		for i := 0; i < n; i++ {
			switch dir {
			case "U":
				ropes[0].y--
			case "D":
				ropes[0].y++
			case "L":
				ropes[0].x--
			case "R":
				ropes[0].x++
			}
			for r := 1; r < len(ropes); r++ {
				head := ropes[r-1]
				tail := ropes[r]
				if tail.isDisconnected(head) {
					p := tail.next(head)
					tail.x += p.x
					tail.y += p.y
					ropes[r] = tail
				} else {
					break
				}
			}
			//draw(ropes)
			cache[ropes[len(ropes)-1]] = true
		}
	}
	return len(cache)
}

func parse(input string) []string {
	return strings.Split(input, "\n")
}

func toInt(s string) int {
	n, err := strconv.Atoi(s)
	if err != nil {
		panic(err)
	}
	return n
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}

func draw(points []point) {
	minX, minY := math.MaxInt, math.MaxInt
	maxX, maxY := 0, 0
	for _, p := range points {
		if p.x > maxX {
			maxX = p.x
		}
		if p.x < minX {
			minX = p.x
		}
		if p.y > maxY {
			maxY = p.y
		}
		if p.y < minY {
			minY = p.y
		}
	}

	res := make([][]string, maxY-minY+1)
	for i := range res {
		res[i] = make([]string, maxX-minX+1)
		for j := range res[i] {
			res[i][j] = "."
		}
	}

	for i, p := range points {
		x := p.x - minX
		y := p.y - minY
		if res[y][x] == "." {
			if i == 0 {
				res[y][x] = "H"
			} else {
				res[y][x] = fmt.Sprint(i)
			}

		}
	}
	fmt.Println()
	for _, row := range res {
		fmt.Println(strings.Join(row, ""))
	}
}

